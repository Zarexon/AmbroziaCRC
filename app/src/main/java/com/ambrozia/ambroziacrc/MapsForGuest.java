        this.map = map;

        DatabaseReference myRef = firebaseDatabase.getReference().child("clicks");
        myRef.addValueEventListener(new ValueEventListener() {
            public void onDataChange(@NonNull DataSnapshot snapshot) {
                for (DataSnapshot snapshot1 : snapshot.getChildren()) {
                    String lat = snapshot.child(snapshot1.getKey()).child("lat").getValue().toString();
                    String lon = snapshot.child(snapshot1.getKey()).child("lng").getValue().toString();
                    LatLng marks = new LatLng(Double.parseDouble(lat), Double.parseDouble(lon));

                    HeatmapTileProvider provider = new HeatmapTileProvider.Builder().data(Collections.singleton(marks)).build();

                    map.addTileOverlay(new TileOverlayOptions().tileProvider(provider));
                    provider.setOpacity(0.5);
                    provider.setRadius(16);
                }

            }

            public void onCancelled(@NonNull DatabaseError error) {

            }
        });

